// ─────────────────────────────────────────────────────────────
//  bitmap_font.h — Embedded 8×8 bitmap font for SDL2 rendering
// ─────────────────────────────────────────────────────────────
//
//  Data layout (DOD):
//    GLYPHS is a flat constexpr array: 95 glyphs × 8 rows = 760 bytes.
//    It fits in 12 cache lines (64 B each). Once the first glyph is
//    touched, most of the table is warm in L1 for subsequent characters.
//    Each byte encodes one row: bit 7 (MSB) = leftmost pixel.
//
//  Rendering strategy:
//    For each row of each glyph we scan for spans of consecutive "on"
//    bits and emit one SDL_RenderFillRectF per span.  Typical letters
//    produce 1–3 spans per row (vs. up to 4 individual rects per row
//    without coalescing), cutting draw-call count by ~50 %.
//
//  Alignment:
//    Total pixel width = (len × (GLYPH_W + GLYPH_SPACING) - GLYPH_SPACING) × scale.
//    Left:   cursor starts at x.
//    Center: cursor starts at x − width / 2.
//    Right:  cursor starts at x − width.
//
//  Zero heap allocations.  All state passed through parameters.
//  Free functions only — no classes, no virtuals.
// ─────────────────────────────────────────────────────────────

