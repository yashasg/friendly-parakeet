// ── text_width ──────────────────────────────────────────────
// Returns the pixel width of a null-terminated string at the given scale.
// O(n) scan, zero allocations.
inline float text_width(const char* text, float scale) noexcept {
    int len = 0;
    while (text[len] != '\0') ++len;
    if (len == 0) return 0.0f;
    // Each char occupies GLYPH_W pixels; gaps of GLYPH_SPACING sit between
    // adjacent characters (no trailing gap after the last one).
    return static_cast<float>(len * (GLYPH_W + GLYPH_SPACING) - GLYPH_SPACING)
         * scale;
}

// ── draw_text ───────────────────────────────────────────────
// Render a null-terminated string using the embedded bitmap font.
//
// Hot-path characteristics:
//   • One SDL_SetRenderDrawColor call per string (not per pixel).
//   • Span-coalescing: consecutive "on" bits in a row become a single
//     SDL_RenderFillRectF call.  A typical uppercase letter averages
//     ~10 rects instead of ~28 individual pixel rects.
//   • No heap allocations, no branches on nullptr, no virtual calls.
//
// Parameters:
//   renderer — SDL renderer (must have blending enabled for alpha < 255)
//   text     — null-terminated ASCII string
//   x, y     — anchor position in logical pixels
//   scale    — uniform scale factor (each font pixel becomes scale×scale)
//   r,g,b,a  — draw color
//   align    — Left: x is left edge
//              Center: x is horizontal midpoint
//              Right: x is right edge
inline void draw_text(SDL_Renderer* renderer,
                      const char*  text,
                      float x, float y,
                      float scale,
                      uint8_t r, uint8_t g, uint8_t b, uint8_t a,
                      TextAlign align = TextAlign::Left) noexcept {
    if (scale <= 0.0f) return;

    // ── 1. Compute length & total width ─────────────────────
    int len = 0;
    while (text[len] != '\0') ++len;
    if (len == 0) return;

    const float char_pitch = static_cast<float>(GLYPH_W + GLYPH_SPACING) * scale;
    const float total_w    = static_cast<float>(
        len * (GLYPH_W + GLYPH_SPACING) - GLYPH_SPACING) * scale;

    // ── 2. Apply alignment offset ───────────────────────────
    float cursor_x = x;
    switch (align) {
        case TextAlign::Left:                                   break;
        case TextAlign::Center: cursor_x -= total_w * 0.5f;    break;
        case TextAlign::Right:  cursor_x -= total_w;            break;
    }

    // ── 3. Set color once for the entire string ─────────────
    SDL_SetRenderDrawColor(renderer, r, g, b, a);

    // ── 4. Render each character ────────────────────────────
    for (int i = 0; i < len; ++i) {
        // Map ASCII to glyph index; replace out-of-range with '?'
        int ch = static_cast<unsigned char>(text[i]);
        if (ch < FIRST_CHAR || ch > LAST_CHAR) ch = '?';
        const uint8_t* glyph = GLYPHS[ch - FIRST_CHAR];

        for (int row = 0; row < GLYPH_H; ++row) {
            uint8_t bits = glyph[row];
            if (bits == 0) continue;        // empty row — skip entirely

            // Span-coalescing: find runs of consecutive set bits
            int col = 0;
            while (col < GLYPH_W) {
                // Skip clear bits
                if (!(bits & (0x80u >> col))) { ++col; continue; }

                // Start of a span
                int span_start = col;
                while (col < GLYPH_W && (bits & (0x80u >> col))) ++col;

                // Emit one rect for the entire span
                SDL_FRect rect = {
                    cursor_x + static_cast<float>(span_start) * scale,
                    y         + static_cast<float>(row)        * scale,
                    static_cast<float>(col - span_start)       * scale,
                    scale
                };
                SDL_RenderFillRectF(renderer, &rect);
            }
        }

        cursor_x += char_pitch;
    }
}

// ── draw_number ─────────────────────────────────────────────
// Convenience: format an int32_t and draw it.
// Uses a 16-byte stack buffer — zero heap allocations.
// Defaults to Center alignment (matches typical HUD score display).
inline void draw_number(SDL_Renderer* renderer,
                        int           number,
                        float x, float y,
                        float scale,
                        uint8_t r, uint8_t g, uint8_t b, uint8_t a,
                        TextAlign align = TextAlign::Center) noexcept {
    char buf[16];
    std::snprintf(buf, sizeof(buf), "%d", number);
    draw_text(renderer, buf, x, y, scale, r, g, b, a, align);
}

